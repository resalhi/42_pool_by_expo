int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 1;
	j = nb;
	if (j > 0)
	{
		while (i * i <= j)
		{
			if (i * i == j)
				return (i);
			i++;
		}
	}
	return (0);
}
