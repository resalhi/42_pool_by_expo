#include <stdio.h>
void	ft_swap(int *a, int *b)
{
	int	swp;

	swp = *a;
	*a = *b;
	*b = swp;
}

int	main()
{
	int a = 50;
	int b = 20;

	ft_swap(&a, &b);
	printf("%d, %d", a, b);
}
